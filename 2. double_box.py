import turtle

anms = turtle.Turtle()

anms.color("red", "yellow")

anms.begin_fill()

anms.forward(90)
anms.left(90)
anms.forward(90)
anms.left(90)
anms.forward(90)
anms.left(90)
anms.forward(90)

anms.penup()
anms.forward(150)
anms.pendown()

anms.forward(90)
anms.left(90)
anms.forward(90)
anms.left(90)
anms.forward(90)
anms.left(90)
anms.forward(90)


anms.end_fill()

turtle.mainloop()
